package com.wildcatcloud.wildcat.auth.handler;

import com.wildcatcloud.wildcat.common.security.handle.AbstractAuthenticationFailureEvenHandler;
import lombok.extern.slf4j.Slf4j;
import org.springframework.security.core.Authentication;
import org.springframework.security.core.AuthenticationException;
import org.springframework.stereotype.Component;

/**
 * @Author WlidcatQin
 * @Date 2019/12/30 20:44
 */
@Slf4j
@Component
public class WildCatAuthenticationFailureEvenHandler  extends AbstractAuthenticationFailureEvenHandler {
    @Override
    public void handle(AuthenticationException authenticationException, Authentication authentication) {
        log.info("用户：{} 登录失败，异常：{}", authentication.getPrincipal(), authenticationException.getLocalizedMessage());
    }
}
